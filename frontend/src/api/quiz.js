/**
 * Получить вопросы для теста (случайные N из всех)
 * @param {number} count
 * @returns {Promise<typeof QUIZ_QUESTIONS>}
 */
export async function getQuizQuestions(count = 5) {
    await new Promise(r => setTimeout(r, 100))
    const shuffled = [...QUIZ_QUESTIONS].sort(() => Math.random() - 0.5)
    return shuffled.slice(0, count)
}

/**
 * Награда за тест
 * @param {number} correct — количество правильных ответов
 * @param {number} total — всего вопросов
 */
export function calcQuizReward(correct, total) {
    const ratio = correct / total
    return {
        lave: Math.floor(ratio * 100),
        respect: Math.floor(ratio * 50),
    }
}
