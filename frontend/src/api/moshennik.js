const DEFAULT_RESPONSE = 'Интересный вопрос. Подумай об этом сам сначала. Зона учит думать своей головой. Если не найдёшь ответ — спроси конкретнее.'

/**
 * Получить ответ MoshennikAI на сообщение
 * @param {string} message
 * @returns {Promise<string>}
 */
export async function getMoshennikResponse(message) {
  // Имитация задержки "печатает..."
  const delay = 600 + Math.random() * 800
  await new Promise(r => setTimeout(r, delay))

  const lower = message.toLowerCase()
  for (const stub of STUB_RESPONSES) {
    if (stub.triggers.some(t => lower.includes(t))) {
      return stub.response
    }
  }
  return DEFAULT_RESPONSE
}
